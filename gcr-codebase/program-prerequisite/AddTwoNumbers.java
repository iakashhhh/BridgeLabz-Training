import java.util.*;
public class AddTwoNumbers{
	public static void main(String[] args){
		Scanner sc = new Scanner(System.in);
		System.out.println("Enter First num : ");
		int num1 = sc.nextInt();
		System.out.println("Enter Second num : ");
		int num2 = sc.nextInt();
		System.out.println("Result : " + (num1+num2));
	}
}
